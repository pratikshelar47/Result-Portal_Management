    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollNo = jLabel10.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/xyz","root","root");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno='"+rollNo+"' and result.rollno='"+rollNo+"'");
            if(rs.next())
            {
                jLabel4.setText(rs.getString("course"));
                jLabel5.setText(rs.getString("branchName"));
                jLabel6.setText(rs.getString("name"));
                jLabel11.setText(rs.getString("fathername"));
                jLabel12.setText(rs.getString("gender"));
                String physics = rs.getString("physics");
                jLabel31.setText(physics);
                String math = rs.getString("math");
                jLabel32.setText(math);
                String em = rs.getString("em");
                jLabel33.setText(em);
                String dbms = rs.getString("dbms");
                jLabel34.setText(dbms);
                String os = rs.getString("os");
                jLabel35.setText(os);
                int total = Integer.parseInt(physics)+Integer.parseInt(math)+Integer.parseInt(em)+Integer.parseInt(dbms)+Integer.parseInt(os);
                jLabel37.setText(total/5+"%");
            }else
            {
                JOptionPane.showMessageDialog(null, "No such student registered");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

